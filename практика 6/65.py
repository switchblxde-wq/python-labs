# Подключаем unittest для запуска набора тестов.
import unittest

# Создаём функцию, которая сравнивает средние чётных и нечётных.
def f4(numbers):
    # Собираем все чётные числа в отдельный список.
    evens = [num for num in numbers if num % 2 == 0]
    # Собираем все нечётные числа в отдельный список.
    odds = [num for num in numbers if num % 2 != 0]
    # Считаем среднее чётных или ноль, если список пустой.
    avg_even = sum(evens) / len(evens) if evens else 0
    # Считаем среднее нечётных или ноль, если список пустой.
    avg_odd = sum(odds) / len(odds) if odds else 0
    # Если среднее чётных больше, возвращаем соответствующее сообщение.
    if avg_even > avg_odd:
        # Возвращаем готовую строку результата.
        return f'Среднее чётных ({avg_even:.2f}) больше среднего нечётных ({avg_odd:.2f})'
    # Если среднее нечётных больше, возвращаем второе сообщение.
    if avg_odd > avg_even:
        # Возвращаем готовую строку результата.
        return f'Среднее нечётных ({avg_odd:.2f}) больше среднего чётных ({avg_even:.2f})'
    # В остальных случаях средние равны.
    return f'Средние равны ({avg_even:.2f})'

# Создаём функцию, которая возвращает максимум из двух чисел.
def f5(a, b):
    # Если первое число больше второго, возвращаем первое.
    if a > b:
        # Возвращаем максимум a.
        return a
    # Иначе возвращаем второе число.
    return b

# Описываем класс с тестами для функций.
class TestFunctions(unittest.TestCase):
    # Проверяем f4 на смешанном списке.
    def test_f4_mixed(self):
        # Сравниваем фактический и ожидаемый результат.
        self.assertEqual(f4([1, 2, 3, 4]), 'Среднее чётных (3.00) больше среднего нечётных (2.00)')

    # Проверяем f4 на списке только чётных.
    def test_f4_only_even(self):
        # Сравниваем фактический и ожидаемый результат.
        self.assertEqual(f4([2, 4, 6]), 'Среднее чётных (4.00) больше среднего нечётных (0.00)')

    # Проверяем f4 на списке только нечётных.
    def test_f4_only_odd(self):
        # Сравниваем фактический и ожидаемый результат.
        self.assertEqual(f4([1, 3, 5]), 'Среднее нечётных (3.00) больше среднего чётных (0.00)')

    # Проверяем f4 на пустом списке.
    def test_f4_empty(self):
        # Сравниваем фактический и ожидаемый результат.
        self.assertEqual(f4([]), 'Средние равны (0.00)')

    # Проверяем f4 на равных средних.
    def test_f4_equal(self):
        # Сравниваем фактический и ожидаемый результат.
        self.assertEqual(f4([1, 2, 3]), 'Средние равны (2.00)')

    # Проверяем f5, когда первое число больше.
    def test_f5_first_greater(self):
        # Сравниваем результат с ожидаемым максимумом.
        self.assertEqual(f5(5, 3), 5)

    # Проверяем f5, когда второе число больше.
    def test_f5_second_greater(self):
        # Сравниваем результат с ожидаемым максимумом.
        self.assertEqual(f5(3, 5), 5)

    # Проверяем f5 на равных числах.
    def test_f5_equal(self):
        # Сравниваем результат с ожидаемым числом.
        self.assertEqual(f5(4, 4), 4)

# Запускаем тесты при прямом запуске файла.
if __name__ == '__main__':
    # Стартуем выполнение всех тестов из этого файла.
    unittest.main()
