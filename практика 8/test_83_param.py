# Подключаем pytest для параметризованных тестов.
import pytest

# Импортируем функции из соседнего файла.
from functions83 import сумма, чётное

# Проверяем функцию суммы на нескольких наборах данных.
@pytest.mark.parametrize('а, б, ожидается', [
    (1, 2, 3),
    (-5, 5, 0),
    (10, -3, 7),
    (0, 0, 0),
])
def test_сумма(а, б, ожидается):
    # Сравниваем фактический результат с ожидаемым.
    assert сумма(а, б) == ожидается

# Проверяем функцию чётности на нескольких значениях.
@pytest.mark.parametrize('число, ожидается', [
    (2, True),
    (7, False),
    (0, True),
    (-4, True),
    (-3, False),
])
def test_чётное(число, ожидается):
    # Сравниваем фактический результат с ожидаемым.
    assert чётное(число) == ожидается
