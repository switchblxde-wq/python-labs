def safe_divide(a, b):  # Пишу маленькую функцию, чтобы её было удобно отлаживать.
    if b == 0:  # Проверяю деление на ноль.
        return 'На ноль делить нельзя'  # Возвращаю безопасный ответ.
    return a / b  # В обычной ситуации делю числа.


if __name__ == '__main__':  # Добавляю точку входа для демонстрации.
    number_a = 20  # Задаю первое число.
    number_b = 5  # Задаю второе число.
    result = safe_divide(number_a, number_b)  # Получаю результат работы функции.
    print('Результат деления:', result)  # Печатаю итог в консоль.
    number_b = 0  # Меняю делитель, чтобы увидеть второй сценарий.
    result = safe_divide(number_a, number_b)  # Снова вызываю функцию.
    print('Проверка деления на ноль:', result)  # Показываю второй итог.
