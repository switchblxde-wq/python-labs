def найти_среднее(числа):  # Делаю функцию, на которой удобно показать отладку.
    if not числа:  # Проверяю, что список не пустой.
        return 0  # Возвращаю ноль для пустого списка.
    сумма = 0  # Создаю переменную для накопления суммы.
    for число in числа:  # Иду по всем элементам списка.
        сумма += число  # Прибавляю текущий элемент к сумме.
    среднее = сумма / len(числа)  # Считаю среднее арифметическое.
    return среднее  # Возвращаю рассчитанное значение.


if __name__ == '__main__':  # Добавляю блок запуска файла напрямую.
    данные = [5, 7, 9, 11]  # Создаю набор данных для примера.
    результат = найти_среднее(данные)  # Вызываю функцию на тестовом списке.
    print('Среднее значение:', результат)  # Вывожу результат для визуальной проверки.
