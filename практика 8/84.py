def add_numbers(a, b):  # Функция для сложения двух чисел.
    return a + b  # Возвращаю сумму.


def multiply_numbers(a, b):  # Функция для умножения двух чисел.
    return a * b  # Возвращаю произведение.


def is_positive(value):  # Функция проверяет, положительное ли число.
    return value > 0  # Возвращаю результат сравнения.


def first_symbol(text):  # Функция берёт первый символ строки.
    if text == '':  # Если строка пустая.
        return ''  # Возвращаю пустую строку.
    return text[0]  # Иначе возвращаю первый символ.


def list_sum(values):  # Функция считает сумму списка.
    total = 0  # Создаю накопитель.
    for item in values:  # Иду по всем элементам.
        total += item  # Прибавляю элемент к накопителю.
    return total  # Возвращаю итог.
