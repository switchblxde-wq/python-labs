def сумма(a, b):  # Пишу первую функцию для сложения.
    return a + b  # Возвращаю сумму двух чисел.


def разность(a, b):  # Пишу вторую функцию для вычитания.
    return a - b  # Возвращаю разность двух чисел.


def произведение(a, b):  # Пишу третью функцию для умножения.
    return a * b  # Возвращаю произведение двух чисел.


def первая_буква(строка):  # Пишу четвёртую функцию для работы со строкой.
    if строка == "":  # Проверяю пустую строку, чтобы не было ошибки индекса.
        return ""  # Возвращаю пустую строку для пустого входа.
    return строка[0]  # Возвращаю первый символ непустой строки.


def длина_списка(список):  # Пишу пятую функцию для списка.
    return len(список)  # Возвращаю количество элементов списка.
