from hypothesis import given  # Подключаю генеративный декоратор для теста.
from hypothesis import settings  # Подключаю настройку количества прогонов.
from hypothesis import strategies as st  # Подключаю стратегии генерации значений.


@settings(max_examples=50)  # Отдельно показываю настройку количества запусков для задания 3.
@given(число=st.integers(min_value=-100, max_value=100))  # Генерирую целые числа в диапазоне.
def test_квадрат_неотрицателен(число):  # Делаю простой тест, чтобы было видно работу max_examples.
    квадрат = число * число  # Вычисляю квадрат числа.
    assert квадрат >= 0  # Проверяю базовое математическое свойство.
