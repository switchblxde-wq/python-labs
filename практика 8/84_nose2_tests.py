import unittest  # Подключаю модуль unittest для тестов.

путь_к_модулю = pathlib.Path(__file__).with_name('84.py')  # Получаю путь к файлу с функциями.
спецификация = importlib.util.spec_from_file_location('практика8_84', путь_к_модулю)  # Создаю описание модуля для загрузки.
