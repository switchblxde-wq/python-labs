# Подключаем модуль doctest для запуска примеров из документации.
import doctest

# Создаём класс счётчика для задания по варианту 5.
class Counter:
    # Описываем работу класса и сразу даём примеры для автоматической проверки.
    """
    Простой счётчик с увеличением и уменьшением значения.

    >>> c = Counter()
    >>> c.get_value()
    0
    >>> c.increment()
    >>> c.get_value()
    1
    >>> c.decrement()
    >>> c.get_value()
    0
    >>> c.decrement()
    >>> c.get_value()
    -1
    """

    # Инициализируем счётчик нулём при создании объекта.
    def __init__(self):
        # Сохраняем текущее значение счётчика.
        self.value = 0

    # Увеличиваем значение счётчика на единицу.
    def increment(self):
        # Прибавляем единицу к текущему значению.
        self.value += 1

    # Уменьшаем значение счётчика на единицу.
    def decrement(self):
        # Вычитаем единицу из текущего значения.
        self.value -= 1

    # Возвращаем текущее значение счётчика.
    def get_value(self):
        # Отдаём сохранённое число счётчика.
        return self.value

# Запускаем doctest из main, потому что вариант нечётный.
if __name__ == '__main__':
    # Запускаем проверку примеров и выводим подробный результат.
    doctest.testmod(verbose=True)
