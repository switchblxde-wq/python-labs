import doctest  # Подключаю модуль, который сам запускает примеры из документации.


class Counter:  # Создаю класс счётчика по условию варианта 5.
    """  # Ниже идёт документация с примерами работы класса.
    Простой счётчик, который умеет увеличиваться и уменьшаться.  # Кратко объясняю назначение класса.

    >>> c = Counter()  # Создаём новый счётчик.
    >>> c.get_value()  # Смотрим стартовое значение.
    0
    >>> c.increment()  # Увеличиваем значение на один.
    >>> c.get_value()  # Проверяем результат после увеличения.
    1
    >>> c.decrement()  # Уменьшаем значение обратно.
    >>> c.get_value()  # Проверяем, что снова ноль.
    0
    >>> c.decrement()  # Уходим в отрицательное значение.
    >>> c.get_value()  # Проверяем, что отрицательные тоже работают.
    -1
    """  # Заканчиваю блок документации.

    def __init__(self):  # Описываю конструктор класса.
        self.value = 0  # При создании счётчик начинается с нуля.

    def increment(self):  # Метод для увеличения значения.
        self.value += 1  # Прибавляю единицу к текущему значению.

    def decrement(self):  # Метод для уменьшения значения.
        self.value -= 1  # Убавляю единицу от текущего значения.

    def get_value(self):  # Метод для получения текущего значения.
        return self.value  # Возвращаю текущее число счётчика.


if __name__ == '__main__':  # Для нечётного варианта запускаю doctest из main.
    doctest.testmod(verbose=True)  # Запускаю проверки из документации с подробным выводом.
