from hypothesis import given  # Подключаю генеративный декоратор для теста.
from hypothesis import settings  # Подключаю настройку числа прогонов.
from hypothesis import strategies as st  # Подключаю стратегии генерации значений.


def умножить_матрицы(a11, a12, a21, a22, b11, b12, b21, b22):  # Делаю функцию умножения двух матриц 2x2.
    c11 = a11 * b11 + a12 * b21  # Считаю элемент в первой строке и первом столбце.
    c12 = a11 * b12 + a12 * b22  # Считаю элемент в первой строке и втором столбце.
    c21 = a21 * b11 + a22 * b21  # Считаю элемент во второй строке и первом столбце.
    c22 = a21 * b12 + a22 * b22  # Считаю элемент во второй строке и втором столбце.
    return c11, c12, c21, c22  # Возвращаю матрицу как кортеж из четырёх чисел.


@settings(max_examples=30)  # Ставлю ограничение на количество сгенерированных наборов.
@given(a=st.integers(min_value=-7, max_value=7), b=st.integers(min_value=-7, max_value=7))  # Генерирую два числа для построения матриц.
def test_матрицы_обычно_не_коммутируют(a, b):  # Проверяю условие варианта 5 для общего случая.
    if a == 0 or b == 0:  # Отбрасываю вырожденные случаи, где матрицы могут коммутировать.
        return  # Просто выхожу из теста для таких значений.
    a11, a12, a21, a22 = 1, a, 0, 1  # Собираю первую матрицу A специального вида.
    b11, b12, b21, b22 = 1, 0, b, 1  # Собираю вторую матрицу B специального вида.
    ab = умножить_матрицы(a11, a12, a21, a22, b11, b12, b21, b22)  # Считаю произведение A * B.
    ba = умножить_матрицы(b11, b12, b21, b22, a11, a12, a21, a22)  # Считаю произведение B * A.
    assert ab != ba  # Проверяю, что в общем случае произведения разные.
