# Подключаем стандартный модуль random для генерации матриц.
import random

# Подключаем pytest для запуска тестов.
import pytest

# Создаём случайную квадратную матрицу заданного размера.
def случайная_матрица(размер):
    # Возвращаем список списков с целыми числами от -3 до 3.
    return [[random.randint(-3, 3) for _ in range(размер)] for _ in range(размер)]

# Умножаем две квадратные матрицы одинакового размера.
def умножить_матрицы(а, б):
    # Определяем размер матрицы.
    размер = len(а)
    # Создаём пустую матрицу результата, заполненную нулями.
    результат = [[0 for _ in range(размер)] for _ in range(размер)]
    # Проходим по строкам первой матрицы.
    for i in range(размер):
        # Проходим по столбцам второй матрицы.
        for j in range(размер):
            # Считаем сумму произведений для одной ячейки результата.
            for k in range(размер):
                # Добавляем очередное произведение в текущую ячейку.
                результат[i][j] += а[i][k] * б[k][j]
    # Возвращаем полученную матрицу.
    return результат

# Проверяем свойство для варианта 5: в общем случае A*B не равно B*A.
@pytest.mark.parametrize('размер', [2, 3, 4])
def test_матрицы_не_коммутируют_в_общем_случае(размер):
    # Делаем несколько попыток, чтобы найти некоммутирующую пару.
    for _ in range(200):
        # Генерируем первую случайную матрицу.
        а = случайная_матрица(размер)
        # Генерируем вторую случайную матрицу.
        б = случайная_матрица(размер)
        # Считаем произведение A*B.
        аб = умножить_матрицы(а, б)
        # Считаем произведение B*A.
        ба = умножить_матрицы(б, а)
        # Если матрицы разные, значит условие подтверждено.
        if аб != ба:
            # Завершаем тест успешно.
            return
    # Если за много попыток не нашли пример, считаем это ошибкой теста.
    pytest.fail('Не удалось найти пример, где A*B не равно B*A')
