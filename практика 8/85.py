import pathlib  # Подключаю работу с путями.
import subprocess  # Подключаю запуск внешних команд.
import sys  # Подключаю данные о текущем интерпретаторе.

папка = pathlib.Path(__file__).resolve().parent  # Определяю папку текущей практики.
команда = [sys.executable, '-m', 'coverage', 'run', '-m', 'pytest', '82.py', '83.py']  # Готовлю команду запуска тестов под coverage.
процесс = subprocess.run(команда, cwd=папка)  # Запускаю измерение покрытия в папке практики.
if процесс.returncode == 0:  # Проверяю, что тесты завершились успешно.
    subprocess.run([sys.executable, '-m', 'coverage', 'report', '-m'], cwd=папка)  # Печатаю отчёт о покрытии в консоль.
else:  # Обрабатываю случай неуспешного запуска.
    print('Не удалось построить отчёт покрытия: проверьте наличие модуля coverage и тестов.')  # Печатаю подсказку на русском.
