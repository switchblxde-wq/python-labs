# Просим пользователя ввести высоты столбцов через пробел.
строка_ввода = input('Введите высоты столбцов через пробел: ')
# Превращаем введённые данные в список чисел с плавающей точкой.
высоты = [float(значение) for значение in строка_ввода.split()]
# Если список пустой, выводим понятное сообщение.
if not высоты:
    # Сообщаем, что строить нечего.
    print('Данные не введены')
# Иначе рисуем простую текстовую диаграмму.
else:
    # Идём по высотам с номерами, чтобы подписать столбцы.
    for номер, высота in enumerate(высоты, start=1):
        # Округляем высоту до целого количества символов.
        длина_полосы = int(round(abs(высота)))
        # Выбираем символ столбца в зависимости от знака числа.
        символ = '█' if высота >= 0 else '░'
        # Печатаем строку диаграммы с номером и значением.
        print(f'{номер:>2}: {символ * длина_полосы} ({высота})')
