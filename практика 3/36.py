# Подключаем математический модуль для работы с синусом, косинусом и числом пи.
import math

# Задаём левую границу диапазона по x.
лево = -3 * math.pi
# Задаём правую границу диапазона по x.
право = 3 * math.pi
# Задаём количество точек для расчёта.
количество = 31
# Считаем шаг между соседними точками.
шаг = (право - лево) / (количество - 1)
# Выводим заголовок таблицы значений.
print('x\ty')
# Перебираем номера точек от 0 до количества.
for i in range(количество):
    # Считаем текущее значение x.
    x = лево + i * шаг
    # Проверяем, что x не равен нулю, чтобы не делить на ноль.
    if x != 0:
        # Вычисляем значение функции в текущей точке.
        y = (math.sin(3 * x) * math.cos(2 * x)) / (3 * x)
        # Выводим x и y с аккуратным форматированием.
        print(f'{x:.4f}\t{y:.6f}')
    # Для x=0 выводим понятную пометку.
    else:
        # Сообщаем, что в нуле значение не определено.
        print(f'{x:.4f}\tне определено')
