# Подключаем csv для чтения таблицы транзакций.
import csv
# Подключаем pathlib для проверки наличия файла.
from pathlib import Path

# Запоминаем путь к ожидаемому CSV с транзакциями.
путь = Path('data/transactions.csv')
# Проверяем, существует ли распакованный CSV.
if not путь.exists():
    # Печатаем подсказку, что данные лежат в архиве.
    print('Файл data/transactions.csv не найден, используем учебные данные')
    # Создаём маленький учебный набор транзакций, чтобы скрипт работал без ошибок.
    транзакции = [
        {'customer_id': '1', 'amount': '-100.0'},
        {'customer_id': '1', 'amount': '-100.0'},
        {'customer_id': '1', 'amount': '100.0'},
        {'customer_id': '2', 'amount': '-40.0'},
    ]
# Если CSV есть, читаем его с помощью DictReader.
else:
    # Открываем файл в кодировке UTF-8.
    with путь.open(encoding='utf-8') as файл:
        # Читаем все строки как словари.
        транзакции = list(csv.DictReader(файл))

# Проверяем, что список транзакций не пустой.
if not транзакции:
    # Печатаем сообщение о пустых данных.
    print('Нет данных для анализа')
# Если данные есть, считаем нужную статистику.
else:
    # Ищем строку с минимальной суммой операции.
    строка_с_минимумом = min(транзакции, key=lambda x: float(x['amount']))
    # Берём id клиента, у которого нашлась минимальная сумма.
    id_клиента = строка_с_минимумом['customer_id']
    # Собираем суммы всех операций этого клиента.
    суммы_клиента = [abs(float(строка['amount'])) for строка in транзакции if строка['customer_id'] == id_клиента]
    # Создаём словарь частот модулей сумм.
    частоты = {}
    # Перебираем все модули сумм клиента.
    for сумма in суммы_клиента:
        # Увеличиваем счётчик для текущей суммы.
        частоты[сумма] = частоты.get(сумма, 0) + 1
    # Находим пару (сумма, частота) с максимальной частотой.
    модуль, количество = max(частоты.items(), key=lambda x: x[1])
    # Печатаем итоговый модуль суммы.
    print(f'Наиболее часто встречающийся модуль суммы: {модуль}')
    # Печатаем количество повторений найденного модуля.
    print(f'Количество повторений: {количество}')
