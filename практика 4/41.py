# Создаём исходный список значений из задания.
значения = ['1', 2, 3.1, 'hi!', 5, -512, 12.42, 'sber', 10.10, 98]
# Создаём новый список индексов от 2 до 11.
индексы = list(range(2, 12))
# Проходим по индексам и значениям одновременно.
for индекс, значение in zip(индексы, значения):
    # Печатаем элемент в формате индекс: значение.
    print(f'{индекс}: {значение}')
