def settings(max_examples=10):  # Делаю упрощённую настройку количества прогонов.
    def декоратор(функция):  # Внутренний декоратор получает тестовую функцию.
        функция._макс_примеров = max_examples  # Сохраняю лимит примеров прямо в функции.
        return функция  # Возвращаю функцию обратно без изменений сигнатуры.
    return декоратор  # Возвращаю готовый декоратор.


def given(**стратегии):  # Создаю упрощённый аналог given с именованными аргументами.
    def декоратор(функция):  # Получаю исходную тестовую функцию.
        def обёртка():  # Делаю функцию без аргументов, чтобы pytest не искал фикстуры.
            списки_значений = {}  # Тут буду хранить наборы примеров для каждого аргумента.
            for имя, стратегия in стратегии.items():  # Перебираю все переданные стратегии.
                списки_значений[имя] = стратегия.примеры()  # Беру примеры из каждой стратегии.
            лимит = getattr(функция, '_макс_примеров', 10)  # Достаю лимит примеров из settings.
            имена = list(списки_значений.keys())  # Сохраняю порядок имён аргументов.
            счётчик = 0  # Создаю счётчик запусков.
            индексы = [0] * len(имена)  # Стартовые индексы для перебора комбинаций.
            while счётчик < лимит:  # Кручу цикл, пока не достиг лимита.
                текущие = {}  # Готовлю словарь аргументов для одного запуска.
                for i, имя in enumerate(имена):  # Прохожусь по аргументам по порядку.
                    значения = списки_значений[имя]  # Беру список возможных значений.
                    текущие[имя] = значения[индексы[i] % len(значения)]  # Выбираю текущее значение.
                функция(**текущие)  # Запускаю исходный тест с текущими данными.
                счётчик += 1  # Увеличиваю количество выполненных запусков.
                for позиция in range(len(индексы)):  # Обновляю индексы, как в счётчике с переносом.
                    индексы[позиция] += 1  # Прибавляю единицу к текущей позиции.
                    if индексы[позиция] < len(списки_значений[имена[позиция]]):  # Если переполнения нет.
                        break  # Выходим, потому что перенос дальше не нужен.
                    индексы[позиция] = 0  # Сбрасываю индекс и переносим в следующий разряд.
        обёртка.__name__ = функция.__name__  # Возвращаю тесту понятное исходное имя.
        return обёртка  # Отдаю обёртку вместо исходной функции.
    return декоратор  # Возвращаю внешний декоратор.
